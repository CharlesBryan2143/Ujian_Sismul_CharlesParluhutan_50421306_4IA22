    <p>
        Raja Ampat adalah sebuah kepulauan yang terletak di Provinsi Papua Barat, Indonesia, yang terkenal sebagai salah satu destinasi wisata bahari terindah di dunia. 
        Kepulauan ini terdiri dari lebih dari 1.500 pulau kecil dan pulau-pulau besar yang dikelilingi oleh laut yang sangat jernih dan kaya akan keanekaragaman hayati. 
        Raja Ampat dikenal sebagai “surga bawah laut” dengan terumbu karang yang luas dan ekosistem laut yang sangat beragam.
    </p>
    <p>
        Terletak di lepas pantai barat laut Pulau Papua, Raja Ampat menjadi tujuan utama bagi para penyelam dan pecinta snorkeling yang ingin menjelajahi keindahan bawah lautnya. 
        Kawasan ini adalah rumah bagi ribuan spesies ikan, terumbu karang yang sehat, serta berbagai makhluk laut eksotis yang langka dan unik. 
        Selain keindahan bawah laut, pemandangan pulau-pulau karst yang menjulang dan hutan tropis yang masih alami menambah daya tarik Raja Ampat sebagai destinasi wisata alam yang menakjubkan.
    </p>
    <p>
        Raja Ampat juga menawarkan berbagai aktivitas wisata seperti trekking, birdwatching untuk melihat burung cendrawasih yang menawan, dan berkunjung ke desa-desa lokal untuk mengenal budaya masyarakat Papua yang kaya dan beragam. 
        Akses menuju Raja Ampat umumnya melalui Kota Sorong, yang merupakan pintu gerbang utama ke kepulauan ini, dengan perjalanan lanjutan menggunakan kapal cepat atau feri.
    </p>
    <p>
        <strong>Lokasi:</strong> Raja Ampat berada di koordinat geografis sekitar 0°30' LS dan 130°45' BT, di Provinsi Papua Barat, Indonesia. 
        Wilayah ini mencakup empat pulau utama yaitu Waigeo, Misool, Salawati, dan Batanta, serta ribuan pulau kecil lainnya yang tersebar di sekitarnya. 
        Raja Ampat merupakan bagian dari Segitiga Terumbu Karang Dunia dan menjadi salah satu kawasan konservasi laut yang sangat penting di Indonesia.
    </p>
