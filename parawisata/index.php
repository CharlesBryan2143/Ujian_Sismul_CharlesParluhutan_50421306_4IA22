    <p>
        Labuan Bajo adalah sebuah kota pelabuhan kecil yang terletak di ujung barat Pulau Flores, Nusa Tenggara Timur, Indonesia. Kota ini merupakan pintu gerbang utama menuju Taman Nasional Komodo, 
        rumah bagi komodo, kadal purba raksasa yang hanya bisa ditemukan di wilayah ini. Keunikan fauna ini menjadikan Labuan Bajo sebagai salah satu destinasi wisata paling eksklusif dan mendunia.
    </p>
    <p>
        Selain komodo, Labuan Bajo menawarkan keindahan alam luar biasa dengan pulau-pulau eksotis seperti Pulau Padar, Pulau Rinca, dan Pulau Kanawa yang dikelilingi oleh perairan jernih berwarna biru toska. 
        Aktivitas seperti island hopping, snorkeling, diving, dan hiking menjadi favorit para wisatawan yang ingin menjelajahi keindahan bawah laut dan panorama dari puncak bukit.
    </p>
    <p>
        Sunset di Labuan Bajo dikenal sebagai salah satu yang terindah di Indonesia, dengan langit oranye kemerahan yang menyinari hamparan laut dan perahu-perahu nelayan yang berlayar perlahan. 
        Suasana tenang dan romantis menjadikan tempat ini sangat cocok untuk liburan, bulan madu, hingga petualangan keluarga.
    </p>
    <p>
        <strong>Lokasi:</strong> Labuan Bajo berada di Kabupaten Manggarai Barat, Provinsi Nusa Tenggara Timur. Secara geografis, letaknya sekitar 8°29′S 119°53′E. 
        Akses menuju Labuan Bajo dapat ditempuh dengan pesawat melalui Bandara Komodo dari kota-kota besar seperti Jakarta, Bali, dan Kupang. Dari pelabuhan Labuan Bajo, wisatawan dapat naik kapal menuju pulau-pulau wisata di sekitarnya.
    </p>
