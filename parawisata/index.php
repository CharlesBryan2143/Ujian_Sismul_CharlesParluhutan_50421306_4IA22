    <p>
        Gunung Bromo adalah salah satu gunung berapi paling terkenal di Indonesia yang terletak di Taman Nasional Bromo Tengger Semeru, Jawa Timur. 
        Gunung ini dikenal dengan pemandangan matahari terbitnya yang spektakuler dan lanskap lautan pasir yang luas dan misterius, menciptakan panorama alam yang sangat ikonik dan menakjubkan.
    </p>
    <p>
        Terletak di ketinggian sekitar 2.329 meter di atas permukaan laut, Gunung Bromo merupakan bagian dari Pegunungan Tengger yang memiliki aktivitas vulkanik yang masih aktif. 
        Wisatawan sering mengunjungi tempat ini untuk menyaksikan matahari terbit dari puncak Penanjakan yang menawarkan pemandangan Gunung Bromo bersama latar belakang Gunung Semeru, gunung tertinggi di Pulau Jawa.
    </p>
    <p>
        Selain keindahan alamnya, kawasan ini juga dihuni oleh suku Tengger yang memiliki budaya unik dan menjalankan upacara keagamaan seperti Yadnya Kasada, yang menjadi daya tarik budaya bagi para pengunjung. 
        Aktivitas trekking, menunggang kuda, dan eksplorasi padang pasir menambah pengalaman petualangan di Gunung Bromo.
    </p>
    <p>
        <strong>Lokasi:</strong> Gunung Bromo terletak di Kabupaten Probolinggo, Pasuruan, dan Lumajang, Jawa Timur, dengan koordinat geografis sekitar 7°56' LS dan 112°56' BT. 
        Kawasan ini dapat diakses dari kota-kota sekitar seperti Malang dan Surabaya, dengan perjalanan darat menuju Desa Cemoro Lawang sebagai titik awal eksplorasi Gunung Bromo.
    </p>
